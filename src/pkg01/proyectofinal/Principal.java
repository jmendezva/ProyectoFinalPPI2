        if (ChoicePro.getSelectedItem().equals("Guerrero")) {
            ataque = 150;
            vida = 1200;
            if (checkboxEsp.getState()) {
                ataque = ataque + 100;
            }
            if (checkboxBas.getState()) {
                ataque = ataque - 100;
            }
        }
        if (ChoicePro.getSelectedItem().equals("Mago")) {
            ataque = 100;
            vida = 1000;
            if (checkboxEsp.getState()) {
                ataque = ataque - 100;
            }
            if (checkboxBas.getState()) {
                ataque = ataque + 100;
            }
        }
        if (ChoicePro.getSelectedItem().equals("Arquero")) {
            ataque = 150;
            vida = 1100;
            if (checkboxEsp.getState()) {
                ataque = ataque + 50;
            }
            if (checkboxBas.getState()) {
                ataque = ataque - 0;
            }
        }
