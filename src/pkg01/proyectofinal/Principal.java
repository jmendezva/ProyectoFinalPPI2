    void Llenar() {
        String archivo = "Status Heroe.txt";
        ArrayList contenido = new ArrayList();
//---------------LLenar datos del Heroe...-----------------------------------------------------------
        boolean existe = false;
        existe = Archivos.TextFiles.existFiles(rutaBase, archivo);
        if (existe) {
            try {

                statusH = Archivos.TextFiles.readFile(rutaBase, archivo);
                for (int i = 0; i < statusH.size(); i++) {
                    contenido.add(statusH.get(i));
                }
                TFHeroe.setText(contenido.get(0).toString());
                TFProfecion.setText(contenido.get(1).toString());
                TFNivel.setText(contenido.get(2).toString());
                TFAtaque.setText(contenido.get(3).toString());
                TFVida.setText(contenido.get(4).toString());
                TFExp.setText(contenido.get(5).toString());
                LabelExp.setText(contenido.get(6).toString());
                
            } catch (IOException ex) {
                Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
        Habilitar();

    }

