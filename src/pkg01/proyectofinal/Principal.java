    private void BLvlActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BLvlActionPerformed
        // TODO add your handling code here:
        String archivo = "Status Heroe.txt";
        ArrayList contenido = new ArrayList();
        Archivos.TextFiles.deleteFiles(rutaBase, archivo);

        int experiencia = 100;
        int nivorg = Integer.parseInt(TFNivel.getText());
        int nivel = Integer.parseInt(TFNivel.getText());
        System.out.println("el nivel es=" + nivel);
        int exp = Integer.parseInt(TFExp.getText());
        System.out.println("exp=" + exp);
        int ataque = Integer.parseInt(TFAtaque.getText());
        int vida = Integer.parseInt(TFVida.getText());
        //        String[] nlvl= LabelExp.getText().toString().split("/");
        int nextlvl = Integer.parseInt(LabelExp.getText());
        System.out.println("Siguiente nivel" + nextlvl);

        exp = exp + experiencia;
        listaEventos.add(TFHeroe.getText()+" a ganado "+exp+" puntos de experiencia");
        System.out.println("expp++" + exp);
        if (exp >= nextlvl) {
            nivel++;
            ataque = ataque + 100;
            vida = vida + 200;
            listaEventos.add(TFHeroe.getText()+" a subido al nivel "+nivel);
        }
        if (nivel > nivorg) {
            nextlvl = nextlvl * 2;
        }
        System.out.println("sig nivel= " + nextlvl);

        contenido.add(TFHeroe.getText());
        contenido.add(TFProfecion.getText());
        contenido.add(Integer.toString(nivel));
        contenido.add(Integer.toString(ataque));
        contenido.add(Integer.toString(vida));
        contenido.add(Integer.toString(exp));
        contenido.add(Integer.toString(nextlvl));

        Archivos.TextFiles.writeFilePlus(rutaBase, archivo, contenido);
        Llenar();

    }//GEN-LAST:event_BLvlActionPerformed

